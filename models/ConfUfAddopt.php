    // Validate form
    protected function validateForm()
    {
        global $Language;

        // Check if validation required
        if (!Config("SERVER_VALIDATE")) {
            return true;
        }
        $validateForm = true;
        if ($this->id->Required) {
            if (!$this->id->IsDetailKey && EmptyValue($this->id->FormValue)) {
                $this->id->addErrorMessage(str_replace("%s", $this->id->caption(), $this->id->RequiredErrorMessage));
            }
        }
        if (!CheckInteger($this->id->FormValue)) {
            $this->id->addErrorMessage($this->id->getErrorMessage(false));
        }
        if ($this->UF->Required) {
            if (!$this->UF->IsDetailKey && EmptyValue($this->UF->FormValue)) {
                $this->UF->addErrorMessage(str_replace("%s", $this->UF->caption(), $this->UF->RequiredErrorMessage));
            }
        }
        if ($this->abbreviation->Required) {
            if (!$this->abbreviation->IsDetailKey && EmptyValue($this->abbreviation->FormValue)) {
                $this->abbreviation->addErrorMessage(str_replace("%s", $this->abbreviation->caption(), $this->abbreviation->RequiredErrorMessage));
            }
        }
        if ($this->countryId->Required) {
            if (!$this->countryId->IsDetailKey && EmptyValue($this->countryId->FormValue)) {
                $this->countryId->addErrorMessage(str_replace("%s", $this->countryId->caption(), $this->countryId->RequiredErrorMessage));
            }
        }
        if (!CheckInteger($this->countryId->FormValue)) {
            $this->countryId->addErrorMessage($this->countryId->getErrorMessage(false));
        }

        // Return validate result
        $validateForm = $validateForm && !$this->hasInvalidFields();

        // Call Form_CustomValidate event
        $formCustomError = "";
        $validateForm = $validateForm && $this->formCustomValidate($formCustomError);
        if ($formCustomError != "") {
            $this->setFailureMessage($formCustomError);
        }
        return $validateForm;
    }

